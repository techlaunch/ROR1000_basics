<?php
// Understanding the Basics: Creating Variables in PHP
// The boolean data type is used to represent TRUE or FALSE

// assigning TRUE
$validUser = TRUE;

// using TRUE in an "if" statement
if ($validUser) {
	echo 'User is Valid';
} else {
	echo 'User is NOT Valid';
}
echo '<br />';

// assigning FALSE
$validUser = FALSE;

// try again
if ($validUser) {
	echo 'User is Valid';
} else {
	echo 'User is NOT Valid';
}
